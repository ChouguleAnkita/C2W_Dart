void main(){
  int i=4;

  if(i<=5 && i>=0){
    if(i==0){
      print("Zero");
    }
    else if(i==1){
      print("One");
    }
    else if(i==2){
      print("Two");
    }
    else if(i==3){
      print("Three");
    }
    else if(i==4){
      print("Four");
    }
    else if(i==5){
      print("Five");
    }
  }
  else{
    print("Entered number is greater than 5");
  }
}